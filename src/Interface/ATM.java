    private void btn_salirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salirActionPerformed
        // TODO add your handling code here:
        if (opcionDepositar == true || opcionRetirar == true){
            
            opcionDepositar = false;
            opcionRetirar = false;
            
            panel_opcion.setVisible(false);
            btn_depositar.setVisible(false);
            btn_retirar.setVisible(true);
            
            btn_uno.setEnabled(false);
            btn_dos.setEnabled(false);
            btn_tres.setEnabled(false);
            btn_cuatro.setEnabled(false);
            btn_cinco.setEnabled(false);
            btn_seis.setEnabled(false);
            btn_siete.setEnabled(false);
            btn_ocho.setEnabled(false);
            btn_nueve.setEnabled(false);
            btn_cero.setEnabled(false);
        
            btn_cancelar.setEnabled(false);
            btn_borrar.setEnabled(false);
            btn_aceptar.setEnabled(false);
            
            txt_cantidad.setText("");
            
        } 
        else{
            JOptionPane.showMessageDialog(null, "GRACIAS");
            System.exit(0);
        }
        
    }//GEN-LAST:event_btn_salirActionPerformed
    
    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        if(txt_cantidad.getText().equals("")){
            opcionDepositar = false;
            opcionRetirar = false;
            
            panel_opcion.setVisible(false);
            btn_depositar.setEnabled(true);
            btn_retirar.setEnabled(true);
            txt_cantidad.setText("");
            
            btn_uno.setEnabled(false);
            btn_dos.setEnabled(false);
            btn_tres.setEnabled(false);
            btn_cuatro.setEnabled(false);
            btn_cinco.setEnabled(false);
            btn_seis.setEnabled(false);
            btn_siete.setEnabled(false);
            btn_ocho.setEnabled(false);
            btn_nueve.setEnabled(false);
            btn_cero.setEnabled(false);
        
            btn_cancelar.setEnabled(false);
            btn_borrar.setEnabled(false);
            btn_aceptar.setEnabled(false);
            
        }
        else if(opcionDepositar == true){
            opcionDepositar = false;
            
            newCantidad = Double.parseDouble(String.valueOf(txt_cantidad.getText()));
            sys.depositar(newCantidad);
            
            txt_saldo.setText(String.valueOf(sys.obtenerSaldo()));
            panel_opcion.setVisible(false);
            txt_cantidad.setText("");
            btn_depositar.setEnabled(true);
            btn_retirar.setEnabled(true);
            
            panel_opcion.setVisible(false);
            btn_depositar.setEnabled(true);
            btn_retirar.setEnabled(true);
            txt_cantidad.setText("");
            
            btn_uno.setEnabled(false);
            btn_dos.setEnabled(false);
            btn_tres.setEnabled(false);
            btn_cuatro.setEnabled(false);
            btn_cinco.setEnabled(false);
            btn_seis.setEnabled(false);
            btn_siete.setEnabled(false);
            btn_ocho.setEnabled(false);
            btn_nueve.setEnabled(false);
            btn_cero.setEnabled(false);
        
            btn_cancelar.setEnabled(false);
            btn_borrar.setEnabled(false);
            btn_aceptar.setEnabled(false);
        }
        else if(opcionRetirar == true){
            opcionRetirar = false;
            
            newCantidad = Double.parseDouble(String.valueOf(txt_cantidad.getText()));
            sys.retirar(newCantidad);
            txt_saldo.setText(String.valueOf(sys.obtenerSaldo()));
            
            panel_opcion.setVisible(false);
            btn_depositar.setEnabled(true);
            btn_retirar.setEnabled(true);
            txt_cantidad.setText("");
            
            btn_uno.setEnabled(false);
            btn_dos.setEnabled(false);
            btn_tres.setEnabled(false);
            btn_cuatro.setEnabled(false);
            btn_cinco.setEnabled(false);
            btn_seis.setEnabled(false);
            btn_siete.setEnabled(false);
            btn_ocho.setEnabled(false);
            btn_nueve.setEnabled(false);
            btn_cero.setEnabled(false);
        
            btn_cancelar.setEnabled(false);
            btn_borrar.setEnabled(false);
            btn_aceptar.setEnabled(false);
        }



        // TODO add your handling code here:
    }//GEN-LAST:event_btn_aceptarActionPerformed

